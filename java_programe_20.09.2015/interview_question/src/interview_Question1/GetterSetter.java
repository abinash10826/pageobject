package interview_Question1;

public class GetterSetter {
	
	private int id;
	private int rollno;
	private String name;
	
	public int getid()
	{
		return id;
	}
	
	public int getrollno()
	{
		return rollno;
	}
	public String getname()
	{
		return name;
	}
	
	public void setid(int id)
	{
		this.id=id;
	}
	public void setrollno(int rollno)
	{
		this.rollno=rollno;
	}
	public void setname(String name)
	{
		this.name=name;
	}

}
